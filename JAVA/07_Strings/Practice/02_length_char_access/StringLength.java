//Find and print the length of a string using `.length()`.
public class StringLength {
    public static void main(String[] args) {
        // Declare and initialize the string variable
        String name = "KpChorDeshChod";

        // Use the length() method to get the number of characters in the string
        int lengthOfString = name.length();

        // Print the length of the string
        System.out.println("Length of the string: " + lengthOfString);
    }
}
