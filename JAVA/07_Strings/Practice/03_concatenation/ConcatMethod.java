//Concatenate two strings using the `.concat()` method and print the result.

public class ConcatMethod {
    public static void main(String[] args) {

        // Declare and initialize two string variables
        String str1 = "hello";
        String str2 = "World";

        // Concatenate the strings using the .concat() method and print the result
        System.out.println(str1.concat(str2));  // Output: helloWorld
    }
}
