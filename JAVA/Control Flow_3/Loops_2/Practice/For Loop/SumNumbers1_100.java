package ForLoop;
//  8. Print the sum of the numbers from 1 to 100.

public class SumNumbers1_100 {

        public static void main(String[] args) {
            int sum =0;
            for (int i = 0; i <=100 ; i++) {
                sum=sum+i;
            }
            System.out.println("The sum of 1 to 100 is " + sum);

        }
    }

