//Replace all spaces in a string with underscores using `.replace()`.

public class ReplaceSpaces {
    public static void main(String[] args) {
        // Original string with spaces
        String sen = "I LOVE MY COUNTRY NEPAL";

        // Replace all spaces (' ') with underscores ('_') and print the result
        System.out.println(sen.replace(" ", "_"));  // Output: I_LOVE_MY_COUNTRY_NEPAL
    }
}
