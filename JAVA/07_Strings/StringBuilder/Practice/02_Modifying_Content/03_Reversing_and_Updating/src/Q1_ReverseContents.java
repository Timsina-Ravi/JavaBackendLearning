// 11. Reverse the entire contents of a `StringBuilder`.

public class Q1_ReverseContents {
    public static void main(String[] args) {

        // Create a StringBuilder with initial content "Ravi"
        StringBuilder stringBuilder = new StringBuilder("Ravi");

        // Reverse the content and print it
        System.out.println(stringBuilder.reverse());  // Output: ivaR
    }
}
