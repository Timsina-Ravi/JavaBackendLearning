//Combine multiple strings and other data types in a sentence and print.
public class MultipleConcatenation {
    public static void main(String[] args) {

        // Declare variables of different types
        int id = 10;
        float marks = 80.5f;
        String name = "Ravi";
        char section = 'B';

        // Concatenate and print all variables in a sentence using +
        System.out.println("ID: " + id + ", Marks: " + marks + ", Name: " + name + ", Section: " + section);
    }
}
