//Find the index of the first occurrence of a character using .indexOf().
//Find the index of the last occurrence of a character using .lastIndexOf().


public class IndexOfExample {
    public static void main(String[] args) {

        String fruit = "banana";

        // Finds the first occurrence of 'a'
        System.out.println(fruit.indexOf('a'));        // Output: 1

        // Finds the last occurrence of 'a'
        System.out.println(fruit.lastIndexOf('a'));    // Output: 5
    }
}
