//Concatenate two strings using the `+` operator and print the result.
public class PlusOperatorConcatenation {
    public static void main(String[] args) {

        // Declare and initialize the first string
        String name = "Ravi";

        // Declare and initialize the second string
        String cast = "Timsina";

        // Concatenate the two strings using the + operator and print the result
        System.out.println(name + cast);  // Output: RaviTimsina
    }
}
