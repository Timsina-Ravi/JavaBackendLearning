//Check if a string starts with a prefix using .startsWith().
//Check if a string ends with a suffix using .endsWith().
public class StartEndCheckExample {
    public static void main(String[] args) {

        String name = "Ravi Timsina";

        // Checks if the string starts with "Ravi"
        System.out.println(name.startsWith("Ravi"));       // Output: true

        // Checks if the string ends with "Timsina"
        System.out.println(name.endsWith("Timsina"));      // Output: true
    }
}
